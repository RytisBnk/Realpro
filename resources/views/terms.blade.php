<h1>TAISYKLĖS IR SĄLYGOS</h1>
<p>1.1. Prašome atidžiai perskaityti šias taisykles ir sąlygas (toliau – „Sąlygos“),  su jomis Pirkėjui sutikus (sutikimu skaitomas varnelės uždėjimo veiksmas paslaugų užsakymo puslapyje, atliekamas perskaičius šias RealPro.lt elektroninės sistemos paslaugų  pirkimo – pardavimo taisykles), yra abiem šalim privalomas teisinis dokumentas, kuriame apibrėžiamos Pirkėjo ir elektroninės paslaugų tiekimo sistemos (toliau vadinamos – „Paslaugos teikėju“) teisės bei pareigos, paslaugų užsakymo ir atsiskaitymo už jas sąlygos, šalių atsakomybė.<br/>
1.2. Paslaugos teikėjas pasilieka teisę keisti, taisyti ir papildyti šias Taisykles. Paslaugos teikėjo daromi pakeitimai siejami su galiojančiais teisės aktų reikalavimais ir neprieštarauja sveikos nuovokos principams.<br/>
1.3. Paslaugos teikėjas neprisiima jokios rizikos ar atsakomybės ir yra nuo jos besąlygiškai atleidžiamas, jei paslaugos pirkėjas iš dalies ar visiškai nesusipažino su Taisyklėmis, nors jam tokia galimybė buvo suteikta.<br/>
1.4. Paslaugos teikėjas turi teisę be įspėjimo apriboti paslaugos Pirkėjo naudojimąsi RealPro.lt sistemos paslaugomis arba panaikinti Pirkėjo registraciją, jeigu Pirkėjas RealPro.lt naudojasi pažeisdamas šias Taisykles, bando pakenkti RealPro.lt sistemos darbo stabilumui ir saugumui.<br/>
1.5. Pirkti Paslaugos teikėjo elektroninėje sistemoje turi teisę žemiau išvardinti asmenys:<br/>
1.5.1. veiksnūs fiziniai asmenys, t.y. asmenys, sulaukę pilnametystės.<br/>
1.5.2. juridiniai asmenys.<br/>
1.6. Sutartis laikoma sudaryta, kai Pirkėjas suformuoja ir pateikia RealPro.lt paslaugų paketo užsakymą, Taisyklėse nustatyta tvarka ir terminais atlieka apmokėjimą ir gauna elektroniniu paštu Paslaugos teikėjo patvirtinimą, kad Pirkėjo užsakymas yra priimtas ir (ar) apmokėjimas gautas.</p>

<h4>2. Pirkimo – pardavimo sutarties sudarymas</h4>
<p>2.1. Pirkimo – pardavimo sutartis tarp Pirkėjo ir Paslaugos teikėjo yra sudaryta nuo Pirkėjo paspaudimo ant nuorodos „UŽSAKYTI“, pasirinkus apmokėjimo būdą, momento. Tais atvejais, kai Pirkėjas nepritaria visoms arba tam tikrai daliai Taisyklių, jis privalo nepateikti užsakymo.<br/>
2.2. Visos Pirkimo – pardavimo sutartys yra saugomos Paslaugos teikėjo internetinės svetainės duomenų bazėje.<br/>
2.3. Pirkėjas, norėdamas užsisakyti paslaugą internetinėje sistemoje RealPro.lt, atitinkamuose Paslaugos teikėjo pateikiamuose informacijos laukuose turi nurodyti paslaugos užsakymo tinkamam įvykdymui būtinus savo duomenis bei užpildyti kitus privalomus laukus. Pirkėjas yra atsakingas, kad registracijos anketoje ir (ar) užsakymo metu pateikti duomenys būtų tikslūs, teisingi ir išsamūs. Jeigu pasikeičia Pirkėjo registracijos formoje pateikti duomenys, jis privalo nedelsdamas juos atnaujinti. Paslaugos teikėjas jokiu atveju nebus atsakingas už žalą, atsiradusią Pirkėjui ir (ar) tretiesiems asmenims dėl to, jog Pirkėjas nurodė neteisingus ir (ar) neišsamius asmens duomenis arba nepakeitė ir nepapildė duomenų jiems pasikeitus.<br/>
2.4. Pirkėjo pateikti asmens duomenys bus tvarkomi laikantis „Lietuvos Respublikos asmens duomenų teisinės apsaugos įstatymo“ nustatytų reikalavimų, kitų Lietuvos Respublikos teisės aktų, reglamentuojančių šių duomenų tvarkymą bei apsaugą.</p>

<h4>3. Paslaugos pirkėjo teisės</h4>
<p>3.1. Pirkėjas turi teisę pirkti paslaugos paketą Paslaugos teikėjo elektroninėje  sistemoje RealPro.lt, nepažeisdami šių Taisyklių sąlygų.<br/>
3.2. Pirkėjas turi teisę, nenurodęs priežasčių, atsisakyti paslaugos pirkimo – pardavimo sandėrio, sudaryto su Paslaugos teikėju, pranešdamas apie tai Paslaugos teikėjui raštu ar elektroniniu paštu,  ne vėliau kaip per 7 darbo dienas nuo paslaugos užsakymo dienos. Paslaugos atšaukimu gali pasinaudoti tik tuo atveju, jeigu nebuvo užsakytos papildomos paslaugos nekilnojamojo turto vertinimas, fotografas, architektas, interjero dizaineris, naudingumo ar garso sertifikatas, teisinė konsultaciją, notaras.  Tuo atveju jeigu skelbimas yra patalpintas į aruodas.lt, skelbiu.lt, domoplius.lt, grąžinama bus 50% paslaugos užsakymo sumos.<br/>
3.3. Taisyklių 3.2. punkte numatyta Pirkėjo teisė įgyvendinama vadovaujantis Ūkio ministro 2001 m. rugpjūčio 17 d. įsakymu Nr. 258 patvirtintomis „Daiktų pardavimo ir paslaugų teikimo, kai sutartys sudaromos naudojant ryšio priemones taisyklėmis”</p>

<h4>4. Pirkėjo pareigos</h4>
<p>4.1. Pirkėjas įsipareigoja sumokėti Paslaugos teikėjui už numatytą užsakymo paketo kainą už paslaugas per nustatytą terminą.<br/>
4.2. Pirkėjas įsipareigoja priimti užsakytas paslaugas. Pirkėjui paslaugos nepriėmus, jis privalo padengti Paslaugos teikėjo nuostolius, susijusius su paslaugos atlikimu.<br/>
4.3. Jeigu pasikeičia registracijos formoje pateikti Pirkėjo duomenys, Pirkėjas privalo juos atnaujinti. Jei dėl klaidingos ar laiku nepakeistos Pirkėjo informacijos Paslaugų teikėjas patyrė nuostolių, Pirkėjas privalo atlyginti Paslaugos teikėjui patirtus nuostolius.<br/>
4.4. Pirkėjas, naudodamasis Paslaugos teikėjo paslaugomis, sutinka su Taisyklėmis bei privalo jų laikytis.<br/>
4.5. Pirkėjas įsipareigoja neperleisti savo prisijungimo duomenų tretiesiems asmenims. Trečioms šalims sužinojus Pirkėjo prisijungimo duomenis, jis nedelsiant privalo pakeisti savo prisijungimo duomenis ir apie prisijungimo duomenų praradimo faktą informuoti Pardavėją elektroniniu paštu.<br/>
4.6. Pirkėjas įsipareigoja užsakytos paslaugos laukti iki 5 darbo dienų.</p>

<h4>5. Pardavėjo teisės</h4>
<p>5.1. Jei Pirkėjas bando pakenkti RealPro.lt elektroninės sistemos darbui bei saugumui, ar pažeidžia savo įsipareigojimus, Paslaugos teikėjas turi teisę sustabdyti Pirkėjo galimybę naudotis elektronine sistema.<br/>
5.2. Paslaugos teikėjas gali laikinai arba iš viso nutraukti elektroninės sistemos veiklą, apie tai iš anksto nepranešęs Pirkėjui.<br/>
5.3. Paslaugos teikėjas turi teisę iškilus neaiškumams, susisiekti su Pirkėju telefonu arba elektroniniu paštu. Jei Paslaugos teikėjas negali susiekti su Pirkėju naudojantis jo kontaktiniais duomenimis, užsakymas anuliuojamas.<br/>
5.4. Jeigu Pirkėjas pasirenka apmokėjimo būdą bankiniu pavedimu, už paslaugas privaloma apmokėti per 24 val. nuo užsakymo pateikimo. Pirkėjui neapmokėjus už paslaugas per nustatytą laiką, Paslaugos teikėjas turi teisę, iš anksto nepranešęs Pirkėjui, anuliuoti užsakymą.<br/>
5.5. Paslaugos teikėjas turi teisę be atskiro įspėjimo, pakeisti akcijų sąlygas arba jas panaikinti, taip pat be jokio įspėjimo stabdyti jų galiojimą.</p>

<h4>6. Paslaugos teikėjo įsipareigojimai</h4>
<p>6.1. Paslaugos teikėjas įsipareigoja, elektroninėje sistemoje nustatytomis sąlygomis, pardavinėti ir skelbti Pirkėjo objektą 6 mėnesius nuo užsakymo dienos.<br/>
6.2. Paslaugos teikėjas įsipareigoja Pirkėjo duomenų neatskleisti trečioms šalims ir gerbti Pirkėjo privatumą.<br/>
6.4. Paslaugos teikėjas  dėl svarbių priežasčių negalėdamassuteikti Pirkėjui užsakytos paslaugos, privalo apie tai pranešti Pirkėjui per 2 darbo dienas nuo užsakymo priėmimo dienos. Paslaugos teikėjas privalo grąžinti Pirkėjui pinigus per 5 darbo dienas nuo apmokėjimo už prekes.</p>

<h4>7. Paslaugų kainos, apmokėjimas už paslaugas</h4>
<p>7.1. Paslaugų teikėjo kainos elektroninėje sistemoje nurodomos eurais su PVM.<br/>
7.2. Paslaugų kainos elektroninėje sistemoje nurodomos prie paslaugos paketo bei galutinai suformuotame užsakymo lange.<br/>
7.3. Pirkėjas elektroninėje sistemoje RealPro.lt už paslaugas atsiskaito:<br/>
7.3.1. banko pavedimu, apmokant nurodytą sumą už paslaugas (Banko pavedimas – tai išankstinis apmokėjimas, kai Pirkėjas, pagal užsakymo patvirtinime pateiktus duomenis ir būtinus mokėjimui rekvizitus, perveda pinigus į RealPro.lt banko sąskaitą: naudojantis internetinės bankininkystės sistema arba artimiausiame banko skyriuje.)</p>

<h4>8. Atsakomybė</h4>
<p>8.1. Atsiradus žalai, kaltoji šalis atlygina kitai šaliai dėl jos kaltės patirtus tiesioginius nuostolius.<br/>
8.2. Pirkėjas pats atsako už veiksmus, atliktus naudojantis elektronine sistema.<br/>
8.3. Paslaugos teikėjas nėra atsakingas tuo atveju, kai nuostoliai kyla dėl to, jog Pirkėjas nesusipažino su šiomis Taisyklėmis, nors tokia galimybė buvo suteikta.</p>

<h4>9. Baigiamosios nuostatos</h4>
<p>9.1. Šių taisyklių pagrindu kylantiems santykiams taikoma LR teisė.<br/>
9.2. Visi nesutarimai, kilę dėl šių taisyklių vykdymo, sprendžiami derybų būdu. Nepavykus susitarti, ginčai sprendžiami LR įstatymų nustatyta tvarka.</p>
